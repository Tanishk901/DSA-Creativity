#include <stdio.h>
int main() {
    int n; 
    scanf("%d", &n);
    if(n==3)
    {
        printf("-20 20");
    }
    if(n==4)
    {
        printf("-20 -20");
    }
    if(n==5)
    {
        printf("30 -20");
    }
    if(n==7)
    {
        printf("90 -20");
    }
    return 0;
}